        <h2 class="nom-roi">Henri IV </h2>
            <p>
                <img src="img\Clovis\clovis1er.jpg" class="image-texte" alt="CLOVIS 1er">
            </p>
            <h3 class="titre">La naissance d'un Roi</h3>
            <p class="paragraphe">
                <span class="tabulation">Henri IV</span> est né le 13 décembre 1553 et a été assassiné le 14 mai 1610 à Paris. (Règne de 16 ans)
                Auparavant, Henri IV fut nommé roi de Navarre (royaume médiéval mise en place en 824 : situé entre la France et l’Espagne) sous le nom de Henri III
                et c’est seulement lors de son couronnement (1594), en tant que roi de France, qu’il a choisi le nom de Henri IV.
                Il était le seul enfant (de sa famille) digne de sang, au trône de France

            </p>
            <h3 class="titre">Son règne</h3>
                <p class="paragraphe">
                    <span class="tabulation">Lors</span> son arrivée au pouvoir, il décide de réunir le royaume de France et de Navarre (dont il était le roi)
                    Il redresse l’économie et la politique française, en s’entourant des bonnes personnes
                    Il gouverne la France d’une main ferme tout en restant très apprécié par le peuple français. 
                    Son pacifisme fait de lui un roi respecté à travers beaucoup de pays. Néanmoins, il reste toujours vigilant envers l’Espagne. De ce fait en 1595, 
                    il déclare officiellement la guerre à l’Espagne, alors que son royaume se déchire toujours. En conséquence, la France et l’Espagne signent un traité de paix, 
                    le 02 mai 1598 mettant fin aux hostilités.

                </p>
                <h3 class="titre">La religion au coeur des tensions</h3>
                <p class="paragraphe">
                    <span class="tabulation">Henri III</span> de Navarre, lors de son enfance se convertit alors au catholicisme, puis retourne au protestantisme quelques années plus tard.
                    Il changea beaucoup de fois de religion en fonction des intérêts du moment. Henri IV, en 1598, décide de signé l'édit de Nantes. En conséquence, 
                    il abjure officiellement le protestantisme.
                </p>
                <p class="paragraphe">
                    <span class="tabulation">Petit</span> point histoire sur l'édit de Nantes, c'est un traité entre la minorité de Protestants et la majorité de Catholiques (présent en France à cette époque) 
                    accordant beaucoup plus de liberté aux Protestants notamment la liberté de culte (dans certains lieux) et il promulgue l’égalité totale entre les protestants et les catholiques.
                    Le but recherché est d'éviter à nouveau les guerres de religions, qui avaient marqué le pays bien avant.

                </p>
                <h3 class="titre">La fin d'un règne</h3>
                <p class="paragraphe">
                        <span class="tabulation">Henri IV</span> a été assassiné en 1610 par François Ravaillac, qui était un fervent catholique. La décision de François Ravaillac
                        a été prise suite à la volonté du roi de vouloir entrer en guerre contre les Habsbourg (lignée d'Espagne et d'Autriche).
                </p> <br>
