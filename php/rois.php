Louis 14 aussi appelé le Roi Soleil ou le Grand aussi surnommé ‘Dieudonné’, est né le 5 septembre 1638 en France à Saint-Germain-en-Laye, ce nom lui vient de Apollon (dieu grec du chant, de la musique, de la lumière et du soleil).
De plus, il chéris le Soleil et le considère comme un astre majestueux et digne de lui et en fera son symbole.
Avant son règne, sa mère Anne d’Autriche avait la régence (elle dirigeait le pays en tant que régent).
Son enfance n’est pas heureuse en partie à cause de la Fronde. (Fronde : période durant 4 ans de révoltes contre la monarchie Française).
Couronné le 7 juin 1654 dans la Cathédrale de Reims.
Il est le roi de France et de Navarre (Royaume médiéval créer en 824 par les Vascons)
Un roi chrétien passionné d’art antique, et de maîtrise du savoir.
Il a vécu dans le célèbre château de Versaille : 
	Ce château est né de la période du Classicisme destiné au roi comme résidence.
	La contuction a débutée en 1623. Il est classé comme monument historique Français.
	Les rois Louis 14, Louis 15 et Louis 16 y vécurent.
	Ce château est étalé sur 63 154 m2 et détient 2300 pièces.
	Le parc du château de Versaille est étalé sur 815 ha, 8000 ha avant le Révolution Française.
	Il comporte des jardins d’une élégance royale.
Ce château à été fabriqué dans le but de faire rayonner la France et de montrer la suprêmacie Française au monde entier.
Sa famille contient un grand nombre d’enfants légitimes et illégitimes.
Il eut 2 femmes : Marie Thérèse d’Autriche et Françoise d’Aubigné.
Cependant, il eut beaucoup de maîtresses et d’aventures avec des femmes, comme Mme de Montespan qui était l’une de ses favorites, il a eu 7 enfants avec elle.
Le roi a passé la moitié de sa vie avec une santé abominable, il vît énormément de médecins pour le soigner durant son règne, il eut la petite vérole très jeune, un ulcère au palais, une sinusite, une dentition effroyable avec des carries abondantes ce qui lui donna une haleine putride, la fistule, la gangrène et d’autre maladies sexuellement trasmissible.
Il créa la compagnie des Indes occidentales pour y exporter des matières prmières.
Il fît la guerre à beaucoup de pays nottament en Europe, ce qui lui permit de  gagner des territoires.
Cette période de guerres a duré 32 ans.
Après son décès le 1er semptembre 1715 à Versaille, le peuple retiendra un roi soleil et son château Versaille, son règne aura duré 72 ans c’est le plus long de tous les rois de France.
Prédécesseur : Louis XIII
Successeur : Louis XV (Louis de France)
Dynastie : Maison de Bourbon
Autre rois de cette dynatie : Henrei 4 Le Grand, Louis Le Juste Louis 15, Louis 16, Louis 18 Chales 10, Louis-Phulippe 1er.
Dynastie : Succession de souverains d'une même famille.
Canonisation : Cérémonie d’ajout d’une personne au catalogue des saints dans l’Eglise universelle
Par le pape.
Saint : Personne suivant une vie conforme à sa religion.
Christianisme : Religion vouée à la vie de Jésus Christ.
Protestanisme : Religion détachée du catholicisme, opposée au pape.
Classicisme : le classicisme est un mouvement culturel qui se développe en France et en Europe autour de l’art entre le 17e et 18e siècle.
Le classicisme a pour principal thème ‘l’honnête homme’ qui est une utopie à atteindre.
On retrouve ce style dans la peinture, le théâtre, mais aussi l’architecture, la poésie, la littérature.
Monarchie : Régime politique avec à son sommet un monarque, un roi héréditaire.

Pourquoi les rois Français ont les mêmes noms ?
Il faut savoir que les noms des ainés sont les noms des grands-pères et les noms des cadets les noms paternels.
Premier roi se nomme : Clovis
Clovis a la même structure que Louis : U et V à changer
	Clovis  Clouis  Louis
